package BTP;

/**
 *
 * @author Daniel
 */
public class BTPAccountType {
    private final int id;
    private final String name;
    
    public BTPAccountType(int id, String name) {
        this.id = id;
        this.name = name;
    }
    
    public int getId() {
        return this.id;
    }
    
    public String getName() {
        return this.name;
    }
}
